        vetor[17] = 6;
        vetor[18] = 0;
        vetor[19] = 6;
        
        int count = 0;
        for (int i = 0; i < n; i++) {
            String a = input.next();
            
            int soma = 0;
            for (int j = 0; j < a.length(); j++) {
                for (int k = 0; k < vetor.length; k++) {
                    int p = Integer.valueOf(a.charAt(j)+"");
                    if (p == vetor[count]) {
                        soma += vetor[count+1];
                        break;
                    }
                    count += 2;
                }
                count = 0;
            }
            System.out.println(soma + " leds");
        }
                
